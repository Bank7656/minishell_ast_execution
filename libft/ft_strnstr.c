#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (ft_strlen(little) < 1)
		return ((char *)big);
	if (big[i] == '\0' || len < ft_strlen(little))
	{
		return (NULL);
	}
	while (big[i] != '\0' && i <= len - 1)
	{
		j = 0;
		while (big[i + j] == little[j] && i + j < len)
		{
			j++;
			if (little[j] == '\0')
				return ((char *)big + i);
		}
		i++;
	}
	return (NULL);
}
